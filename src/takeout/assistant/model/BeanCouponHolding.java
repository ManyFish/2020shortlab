package takeout.assistant.model;

public class BeanCouponHolding {
	
	private int coupon_id;
	private int user_id;
	private int business_id;
	private float couponp_amount;
	private int cp_num;
	public int getCoupon_id() {
		return coupon_id;
	}
	public void setCoupon_id(int coupon_id) {
		this.coupon_id = coupon_id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getBusiness_id() {
		return business_id;
	}
	public void setBusiness_id(int business_id) {
		this.business_id = business_id;
	}
	public float getCouponp_amount() {
		return couponp_amount;
	}
	public void setCouponp_amount(float couponp_amount) {
		this.couponp_amount = couponp_amount;
	}
	public int getCp_num() {
		return cp_num;
	}
	public void setCp_num(int cp_num) {
		this.cp_num = cp_num;
	}
	
	
}
