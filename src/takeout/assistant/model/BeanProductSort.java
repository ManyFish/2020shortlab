package takeout.assistant.model;

public class BeanProductSort {

	
	private int category;
	private String category_column;
	private int productnum;
	
	public int getCategory() {
		return category;
	}
	public void setCategory(int category) {
		this.category = category;
	}
	public String getCategory_column() {
		return category_column;
	}
	public void setCategory_column(String category_column) {
		this.category_column = category_column;
	}
	public int getProductnum() {
		return productnum;
	}
	public void setProductnum(int productnum) {
		this.productnum = productnum;
	}
	
	
}
