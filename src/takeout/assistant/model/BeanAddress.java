package takeout.assistant.model;

public class BeanAddress {
	
	private int address_id;
	private int user_id;
	private String add_province;
	private String add_city;
	private String add_district;
	private String add_address;
	private String add_contact;
	private String add_phone;
	public int getAddress_id() {
		return address_id;
	}
	public void setAddress_id(int address_id) {
		this.address_id = address_id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getAdd_province() {
		return add_province;
	}
	public void setAdd_province(String add_province) {
		this.add_province = add_province;
	}
	public String getAdd_city() {
		return add_city;
	}
	public void setAdd_city(String add_city) {
		this.add_city = add_city;
	}
	public String getAdd_district() {
		return add_district;
	}
	public void setAdd_district(String add_district) {
		this.add_district = add_district;
	}
	public String getAdd_address() {
		return add_address;
	}
	public void setAdd_address(String add_address) {
		this.add_address = add_address;
	}
	public String getAdd_contact() {
		return add_contact;
	}
	public void setAdd_contact(String add_contact) {
		this.add_contact = add_contact;
	}
	public String getAdd_phone() {
		return add_phone;
	}
	public void setAdd_phone(String add_phone) {
		this.add_phone = add_phone;
	}

	
}
