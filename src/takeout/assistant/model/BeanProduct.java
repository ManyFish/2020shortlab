package takeout.assistant.model;

public class BeanProduct {

	private int pro_id;
	private int category;
	private int business_id;
	private int pro_category;
	private String pro_name;
	private float pro_price;
	private float dis_price;
	private int pro_num;
	public int getPro_id() {
		return pro_id;
	}
	public void setPro_id(int pro_id) {
		this.pro_id = pro_id;
	}
	public int getCategory() {
		return category;
	}
	public void setCategory(int category) {
		this.category = category;
	}
	public int getBusiness_id() {
		return business_id;
	}
	public void setBusiness_id(int business_id) {
		this.business_id = business_id;
	}
	public int getPro_category() {
		return pro_category;
	}
	public void setPro_category(int pro_category) {
		this.pro_category = pro_category;
	}
	public String getPro_name() {
		return pro_name;
	}
	public void setPro_name(String pro_name) {
		this.pro_name = pro_name;
	}
	public float getPro_price() {
		return pro_price;
	}
	public void setPro_price(float pro_price) {
		this.pro_price = pro_price;
	}
	public float getDis_price() {
		return dis_price;
	}
	public void setDis_price(float dis_price) {
		this.dis_price = dis_price;
	}
	public int getPro_num() {
		return pro_num;
	}
	public void setPro_num(int pro_num) {
		this.pro_num = pro_num;
	}
	
	
}
